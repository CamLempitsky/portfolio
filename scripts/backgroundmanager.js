class BackgroundManager {
    constructor() {
        //This is how to construct a 2d array in JS, yuck.
        this.data = [];
        for (let i = 0; i < 100; i++) {
            this.data.push([]);
        }

        this.fillArray(mapData);

        this.width = 100;
        this.height = 100;

        this.tilemap = ASSET_MANAGER.getAsset("../sprites/tiles.png");
    }

    /**
     * Adds all the background tiles as entities to the background layer of the game engine.
     */
    addBackgroundTiles() {
        for(let i = -50; i < 50; i++) {
            gameEngine.addEntity(new BackgroundTile({x: -5, y: i},
                new Dimension(16, 16),
                this.tilemap, {x:0, y:1}),  Layers.BACKGROUND);
            gameEngine.addEntity(new BackgroundTile({x: -4, y: i},
                new Dimension(16, 16),
                this.tilemap, {x:1, y:1}),  Layers.BACKGROUND);
            gameEngine.addEntity(new BackgroundTile({x: -3, y: i},
                new Dimension(16, 16),
                this.tilemap, {x:2, y:1}),  Layers.BACKGROUND);
        }

        // for(let row = 0; row < this.height; row++) {
        //     for(let col = 0; col < this.width; col++) {
        //         gameEngine.addEntity(new BackgroundTile({x: col-this.height/2, y: row-this.height/2},
        //             new Dimension(16, 16),
        //             this.tilemap, {x:5, y:3}),  Layers.BACKGROUND);
        //     }
        // }
        for(let row = 0; row < this.height; row++) {
            for(let col = 0; col < this.width; col++) {
                gameEngine.addEntity(new GrassTile({x: col-this.height/2, y: row-this.height/2},
                    new Dimension(16, 16)),  Layers.BACKGROUND);
            }
        }
    }

    fillArray(text) {
        let ctr = 1;
        for(let row = 0; row < 100; row++) {
            for(let col = 0; col < 100; col++) {
                this.data[row][col] = text[ctr++];
            }
            ctr++;
        }
    }
}

class GrassTile extends Entity{
    constructor(pos, size) {
        super(pos, size);
        this.sprite = ASSET_MANAGER.getAsset("../sprites/grass_1.png")
        this.pos.x *= TILE_SIZE;
        this.pos.y *= TILE_SIZE;
    }

    draw(ctx) {
        ctx.drawImage(this.sprite, 0, 0, 192, 192, this.getScreenPos().x, this.getScreenPos().y, TILE_SIZE, TILE_SIZE);
    }

    update() {

    }
}

