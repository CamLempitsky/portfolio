"use strict";

class BackgroundManager {
    constructor() {
        this.data = [];
        for (let i = 0; i < 100; i++) {
            this.data.push([]);
        }

        this.fillArray(mapData);

        this.width = 100;
        this.height = 100;

        this.sprites = [];
        this.sprites[1] = ASSET_MANAGER.getAsset("../sprites/grass.png");
        this.sprites[2] = ASSET_MANAGER.getAsset("../sprites/sand.jpg");
    }

    addBackgroundTiles() {
        for(let row = 0; row < 100; row++) {
            for(let col = 0; col < 100; col++) {
                gameEngine.addEntity(new Tile(col-this.height/2, row-this.width/2,
                    this.sprites[this.data[col][row]]), Layers.BACKGROUND);
            }
        }
    }

    fillArray(text) {
        let ctr = 1;
        for(let row = 0; row < 100; row++) {
            for(let col = 0; col < 100; col++) {
                this.data[col][row] = text[ctr++];
            }
            ctr++;
        }
    }
}
